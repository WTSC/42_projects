#include "libft.h"

char *ft_capitalize(char *s)
{
	if (s != NULL)
		if (ft_isalpha(s[0]) && s[0] >= 97 && s[0] <= 122)
			s[0] = ft_toupper(s[0]);
	return (s);
}
